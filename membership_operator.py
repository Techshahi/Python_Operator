x=["banana", "apple"]

#in operator:return true if sequence with the specified value is present in object/variable

print("banana" in x) #true because banana available
print("pineapple" in x) #false because pineapplee not  available

#not in operator:return true if specified value is not present in object

print("pineapple" not in x)  #true because pineapple not  available
print("apple" not in x)  #false because apple are  available